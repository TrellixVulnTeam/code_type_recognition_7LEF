dic.add('@')
dic.add('#')
dic.add('$')
dic.add('%')
dic.add('^')
dic.add('&')
dic.add('*')
dic.add('(')
dic.add(')')
dic.add('-')
dic.add('_')
dic.add('+')
dic.add('=')
dic.add('{')
dic.add('}')
dic.add('[')
dic.add(']')
dic.add('|')
dic.add('\\')
dic.add(':')
dic.add(';')
dic.add('"')
dic.add('\'')
dic.add('<')
dic.add('>')
dic.add(',')
dic.add('.')
dic.add('?')
dic.add('/')
				
for ele in sorted(dic):
    output.write(ele+'\n')
output.close()